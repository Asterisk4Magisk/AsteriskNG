package data

import androidx.room.Entity
import androidx.room.PrimaryKey
import app.AppState

internal const val SingletonEntityId = "default"

@Entity(tableName = "app_settings")
internal data class AppSettingsEntity(
    @PrimaryKey val id: String,
    val colorMode: Int,
    val languageMode: Int,
    val seedIndex: Int,
    val nextSubscriptionGroupId: Int,
    val enableAllProxyGroup: Boolean,
    val runMode: Int,
    val enableResolveProxyServerDomain: Boolean,
    val enableVpnLocalDns: Boolean,
    val localProxyPort: String,
    val enableDynamicLocalProxyPort: Boolean,
    val localProxyListenAllInterfaces: Boolean,
    val localProxyUsername: String,
    val localProxyPassword: String,
    val enableVpnAppendHttpProxy: Boolean,
    val vpnMtu: String,
    val vpnDefaultDns: String,
    val vpnIpv4Cidr: String,
    val vpnIpv6Cidr: String,
    val nextProxyServerId: Int,
    val selectedProxyServerId: Int,
    val routeDomainStrategy: Int,
    val nextRouteRuleId: Int,
    val coreLogLevel: Int,
    val enableAccessLog: Boolean,
    val resourceFileSource: Int,
    val enableSniffing: Boolean,
    val enableSniffingRouteOnly: Boolean,
    val enableMux: Boolean,
    val muxConcurrency: String,
    val muxXudpConcurrency: String,
    val muxXudpProxyUdp443: Int,
    val enableFragment: Boolean,
    val fragmentPackets: String,
    val fragmentLength: String,
    val fragmentInterval: String,
    val enableIpv6: Boolean,
    val enableIpv6Prefer: Boolean,
    val enableFakeDns: Boolean,
    val transparentProxyPort: String,
    val enableTproxyBootScript: Boolean,
    val enableSocks5Proxy: Boolean,
    val socks5ProxyPort: String,
    val enableHttpProxy: Boolean,
    val httpProxyPort: String,
    val proxyAppListMode: Int,
) {
    companion object {
        fun from(state: AppState): AppSettingsEntity {
            return AppSettingsEntity(
                id = SingletonEntityId,
                colorMode = state.colorMode,
                languageMode = state.languageMode,
                seedIndex = state.seedIndex,
                nextSubscriptionGroupId = state.nextSubscriptionGroupId,
                enableAllProxyGroup = state.enableAllProxyGroup,
                runMode = state.runMode,
                enableResolveProxyServerDomain = state.enableResolveProxyServerDomain,
                enableVpnLocalDns = state.enableVpnLocalDns,
                localProxyPort = state.localProxyPort,
                enableDynamicLocalProxyPort = state.enableDynamicLocalProxyPort,
                localProxyListenAllInterfaces = state.localProxyListenAllInterfaces,
                localProxyUsername = state.localProxyUsername,
                localProxyPassword = state.localProxyPassword,
                enableVpnAppendHttpProxy = state.enableVpnAppendHttpProxy,
                vpnMtu = state.vpnMtu,
                vpnDefaultDns = state.vpnDefaultDns,
                vpnIpv4Cidr = state.vpnIpv4Cidr,
                vpnIpv6Cidr = state.vpnIpv6Cidr,
                nextProxyServerId = state.nextProxyServerId,
                selectedProxyServerId = state.selectedProxyServerId,
                routeDomainStrategy = state.routeDomainStrategy,
                nextRouteRuleId = state.nextRouteRuleId,
                coreLogLevel = state.coreLogLevel,
                enableAccessLog = state.enableAccessLog,
                resourceFileSource = state.resourceFileSource,
                enableSniffing = state.enableSniffing,
                enableSniffingRouteOnly = state.enableSniffingRouteOnly,
                enableMux = state.enableMux,
                muxConcurrency = state.muxConcurrency,
                muxXudpConcurrency = state.muxXudpConcurrency,
                muxXudpProxyUdp443 = state.muxXudpProxyUdp443,
                enableFragment = state.enableFragment,
                fragmentPackets = state.fragmentPackets,
                fragmentLength = state.fragmentLength,
                fragmentInterval = state.fragmentInterval,
                enableIpv6 = state.enableIpv6,
                enableIpv6Prefer = state.enableIpv6Prefer,
                enableFakeDns = state.enableFakeDns,
                transparentProxyPort = state.transparentProxyPort,
                enableTproxyBootScript = state.enableTproxyBootScript,
                enableSocks5Proxy = state.enableSocks5Proxy,
                socks5ProxyPort = state.socks5ProxyPort,
                enableHttpProxy = state.enableHttpProxy,
                httpProxyPort = state.httpProxyPort,
                proxyAppListMode = state.proxyAppListMode,
            )
        }
    }
}
